public class Switch1 {
    public static void main(String[] args){
        String dia = "lala";

        switch(dia) {
            case "Domingo": System.out.println("It is Sunday!");
            break;
            case "Kibe": System.out.println("It is Kibe");
            break;
            default: System.out.println("qualé nem é kibe nem Domingo, é coxinha então");
        }
    }
}
